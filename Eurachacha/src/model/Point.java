package model;

public class Point {
	   private int point;

	   public Point() { }
	   
	   public Point(int point) {
	      super();
	      this.point = point;
	   }

	   public int getPoint() {
	      return point;
	   }

	   public void setPoint(int point) {
	      this.point = point;
	   }
	   
}